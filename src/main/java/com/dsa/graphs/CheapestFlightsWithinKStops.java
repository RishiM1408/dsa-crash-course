package com.dsa.graphs;

import java.util.*;

/**
 * Cheapest Flights Within K Stops (LeetCode 787)
 * 
 * Modified Dijkstra or Bellman-Ford (here BFS/Dijkstra variant).
 */
public class CheapestFlightsWithinKStops {

    public int findCheapestPrice(int n, int[][] flights, int src, int dst, int k) {
        // Adj List: Node -> {Neighbor, Cost}
        Map<Integer, List<int[]>> adj = new HashMap<>();
        for (int[] f : flights) {
            adj.computeIfAbsent(f[0], key -> new ArrayList<>()).add(new int[] { f[1], f[2] });
        }

        // Cost array to track min cost to reach node i within j stops.
        // Instead, track min cost to reach node i with strictly fewer stops.
        int[] prices = new int[n];
        Arrays.fill(prices, Integer.MAX_VALUE);
        prices[src] = 0;

        Queue<int[]> q = new LinkedList<>();
        // {Node, Cost, Stops}
        q.offer(new int[] { src, 0, 0 });

        while (!q.isEmpty()) {
            int[] curr = q.poll();
            int u = curr[0];
            int cost = curr[1];
            int stops = curr[2];

            if (stops > k)
                continue;

            if (adj.containsKey(u)) {
                for (int[] edge : adj.get(u)) {
                    int v = edge[0];
                    int w = edge[1];
                    // Pruning: if we found a cheaper way to reach v with <= steps, update
                    if (cost + w < prices[v]) {
                        prices[v] = cost + w;
                        q.offer(new int[] { v, prices[v], stops + 1 });
                    }
                }
            }
        }

        return prices[dst] == Integer.MAX_VALUE ? -1 : prices[dst];
    }
}
